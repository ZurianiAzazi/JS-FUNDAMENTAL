//Primitive

// String 
const userName = 'sara';
console.log(userName);
console.log(typeof userName);

//Number
const age = 45;
console.log(age);
console.log(typeof age);

//Boolean
const isStudent = true;
console.log(isStudent);
console.log(typeof isStudent);

//Null
const car = null;
console.log(car);
console.log(typeof car);

//undefined
let color;
console.log(color);
console.log(typeof color);

//symbol
const sym = Symbol();
console.log(sym);
console.log(typeof sym);


//Ref types (objs)
//array

const numbers = [1,2,3,4,5,6];
console.log(numbers);
console.log(typeof numbers);

//object
const student = {
    firstName : 'sara',
    age : 20,
    address : {
        city:'Kuala lumpur',
        country:'Malaysia'
    }
};

console.log(student);
console.log(typeof student);




